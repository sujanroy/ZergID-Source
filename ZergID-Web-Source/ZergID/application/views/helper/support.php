<p><b>Q:</b>   My game is not listed.</p>

<p class="space_bottom_support"><b>A:</b>   We are adding games as quickly as we can. Please request your game to be 
added by selecting “Game not listed” at the bottom of the game selection 
menu and we will get to it as quickly as possible.</p>

<p><b>Q: </b>  How do I select Top Characters and Top Guilds?</p>

<p class="space_bottom_support"><b>A:</b>   You can choose up to 3 of your Characters and/or Guilds to appear on your 
profile page. You can select which of your Characters and/or Guilds will 
appear here by clicking the star next to your Characters/Guilds on your 
Character/Guild page.</p>

<p><b>Q:</b>   When I created my character or RSVPd to an event the only roles I could 
choose were Tank/Healer/DPS/Hybrid, but my game doesn’t have these 
roles, what gives?</p>

<p class="space_bottom_support"><b>A:</b>   While, tank/healer/DPS, the holy trinity of MMO’s for many years, we 
understand that many games are breaking the meta and other games have 
entirely different role structures. For character creation we added the 
“Hybrid” role to help round out “Support” characters that perform multiple 
roles. We are working on customizing character/event creation for each 
game, but we needed to start somewhere and felt that tank/healer/DPS 
captured the concept in the largest number of games.</p>

