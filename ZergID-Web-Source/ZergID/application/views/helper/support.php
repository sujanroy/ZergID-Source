<p><b>Q:</b>   How does the RSVP Approval system work?</p>

<p class="space_bottom_support"><b>A:</b>   Users will receive an in system message asking them to RSVP. Once they 
click on it, they are asked to choose their potential roles in the event. They 
can choose one or more of the roles (Tank/Healer/DPS)</p>

<p class="space_bottom_support">Once people have RSVPd, the event leader can set up the team. The 
potential roles people have selected will be shown to the event creator</p>

